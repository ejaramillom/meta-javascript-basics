// class 05: declaring variables in javascript

// javascript understand this as string
"John"

// i can declare a variable without content
var person;

// then i can add value to the variable
var person = "John"

// i can tell the console in the browser to show me the variable value
console.log("Hello", person)

var greeting = "hello"

console.log(greeting, person)

// we can also reassign values to existing variables
person = 'james'
greeting = 'hi'

console.log(greeting, person)
